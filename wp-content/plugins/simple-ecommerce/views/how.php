<h1>How this plugin works</h1>
<p>
    This plugin automatically publishes wordpress posts to profiles/pages/groups on Facebook, Twitter, Instagram,
    Google+,
    LinkedIn, Tumblr and other wordpress sites with using simpletools.com<br/><br/>

    You do not need to create app for every social media site.simpleToolsEcommerce handles everything for you. You only need
    simpleToolsEcommerce API key it to work for you

</p>


<h3>How to use</h3>
<p>

<ul>
    <li>Make sure that you have authorized your social accounts on simpletools.com</li>
    <li>Prepare your wordpress post as usual</li>
    <li>After you have finished making your wordpress post , select the social media
        account that you are going to send
    </li>
    <li>Hit the "Update" button</li>

</ul>
<br/>
Plugin will send your post to your selected social
accounts

</p>


